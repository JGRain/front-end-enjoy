<h2 id="初衷" tabindex="-1"><a class="header-anchor" href="#初衷" aria-hidden="true">#</a> 初衷</h2>
<p>记得在两年多前，我找到编辑小姐姐跟她说想写本自传。编辑问了下我的情况，说不建议这个年纪出自传。接下来我就在自己的博客上，偶尔写点工作上的思考和记录。</p>
<p>去年的时候，编辑看到我的博客，说挺喜欢工作那块内容，问要不写成一本书呢？然后我们聊着聊着，她才发现我一开始找她说想写的“自传”，其实便是这些工作经历和思考，我俩都笑了。</p>
<p>后来，我便开始写起了这本书，主要包括一些工作原则和方法、团队协作、职业规划等内容。</p>
<p>说起来，最开始为什么想写这样的内容呢？主要是因为自己这几年的工作经历也比较折腾，认识和学到了很多。但反观身边的很多小伙伴，尤其是刚毕业的应届生们，他们会存在很多很多的疑惑，也没有人告诉他们该怎么做，很多时候会陷入自我怀疑的困境。</p>
<p>他们遇到的这些问题，有些只需要调整下自身的工作方式和状态，有一些需要通过有效的沟通去解决，还有一些则是大环境下的常见问题。但是职场本身就是一个竞争很大的地方，如果身处一个不那么友好的团队，对刚入职场的年轻人来说，工作没得到正反馈、沟通的欠缺、甚至被打压等，很可能会对职业生涯造成不小的影响。</p>
<p>职场工作和校园学习相差很远，刚开始工作的那几年，很可能就决定了以后对工作、对这个行业的认知和价值观。很多人都会遇到一些不公正的事情，但是非正确只能由自己来判断，因此很多时候我们都会向身边人看齐。因为“大家都是这么做的”，很多不该发生的事情也就顺理成章地继续存在下去。</p>
<p>又或者是，长时间无法脱离忙碌的工作状态，生活失去了重心而不知所措。对工作认真负责，这是作为一个打工人的基本原则，但它的边界在哪里却需要我们自己进行探索。有些人认为下班该把工作放一边、陪陪家人，有些人则认为下班后也该多思考下工作，还有人认为即使下班了也该随时随地支持工作。</p>
<p>很多很多的事情，它们都没有标准答案，都需要每个人自己去进行探索和思考。</p>
<p>因此，我把自己的工作方法和思考写下来，希望能对一些正感到困惑的人给到帮助。这就是这本书的初衷，我非常希望在遇到一些“不对劲”的事情时，他们能少一些的自我怀疑，接受预期之外的事情发生，同时能坚持住自己的初心。</p>
<h2 id="学着画插画" tabindex="-1"><a class="header-anchor" href="#学着画插画" aria-hidden="true">#</a> 学着画插画</h2>
<p>这些内容早就刻在我的脑海里，因此将它们写下来并没有花很长的时间。实际上，我每次写书速度都超乎编辑们的想象。</p>
<p>编辑小姐姐也很喜欢我写的内容，她跟我说，因为都是纯文字，如果加点插画可能会更好。我说我只会画一些很幼稚的画，她说能表达清楚就可以，不用画的多漂亮。</p>
<p>因此，我便又学起了插画，结合书中的一些内容，加上我家的猫，在适当的地方加上了一些插画内容。画画是一件很好玩的事情，也因为这个契机，我后来开始画起来我家猫的表情包，也给编辑帮其它书画过插画。</p>
<p>到这，书中的文字和插画，其实都是我特别喜欢的内容。我很希望，这本书能通过印刷的方式出版，然后可以在手中安静地读，静静地翻页。</p>
<p>但事实并非如此。</p>
<h2 id="纸质书的困境" tabindex="-1"><a class="header-anchor" href="#纸质书的困境" aria-hidden="true">#</a> 纸质书的困境</h2>
<p>尽管我和编辑小姐姐两个都很喜欢这本书，但是对于纸质书来说，成本和收益摆在眼前，而根据出版社的调研统计，软技能远不如硬技能有市场。</p>
<p>我们都很难过，然后编辑提出要不加一些硬技能的内容，于是我尝试在原有的内容基础上进行调整。我们尝试了好几次的调整，包括把前端技术从入门到提升的部分都加进去了，这些内容大多数是由我的技术博客整理而来。</p>
<p>最终这本书的内容瞬间翻了倍，变成了三大部分：前端基础和入门、提升硬实力、必备软实力。</p>
<p>而我最喜欢的部分，依然是最初的软技能部分。但结果依然是，这部分不能进行纸质书出版，因为没有市场。</p>
<p>我们有考虑过，将这部分的内容进行开源，就像之前《深入理解 Vue.js 实战》这本书一样。对我来说，有一些很天真的想法和坚持，正如 Vue.js 这本书都写完了，最后我却征求编辑小姐姐的同意，将它进行了开源。</p>
<p>那时候，我认为通过免费开源能给很多人带来帮助，但事实是大家并没有多少途径了解到这本书。这也是我现在会开始出版书、做专栏的一个原因，因为出版社和专栏团队它们会负责推广，这是我很难做到的事情。</p>
<p>其实做这些并不会有很多的收益，而我自身也不爱受约束，每次中途都有无数次想要放弃、直接转开源的想法。但不得不说，我需要这个阶段，让这些内容有机会被大家了解到，才可以让一些内容被更多的人看到，也希望能真正地给到一些帮助。</p>
<p>让我很开心的事情，并不是这些内容卖出去多少份，也不是被很多人认识。而是偶尔收到的一封邮件、一句赞赏的话，告诉我他们喜欢我的文章，说这些有帮助到他们，这才是我想要坚持下去的原因。</p>
<p>在以前，我一直是自己最忠实的读者，现在我有一些小伙伴了。不需要很多人，即使还有一个人在看，我也希望能继续写下去。</p>
<p>也因此，这本书最后转为电子书的方式出版，因为电子书并不存在纸质书那样对销量的要求。</p>
<h2 id="转场电子书" tabindex="-1"><a class="header-anchor" href="#转场电子书" aria-hidden="true">#</a> 转场电子书</h2>
<p>转到电子书专场，也可能不会像纸质书那样有更多的渠道推广，但没试过的事情总得去试试才知道，所以我先试试看。这是我这几年来的一个爱好，去尝试很多事情，学会画插画、出版纸质书、出版电子书、出版专栏、学着做课程、学着做视频，等等。这些对我来说，都能学到不少东西，也让我从以前“自己”的角度，学会了解“读者”的角度。</p>
<p>也特别感谢电子书的编辑小姐姐，也一样喜欢书中的内容，以及我画的一些插画，努力地帮我推上线。</p>
<p>我给这本书的封面画上了我家的猫，希望看这本书的大家也都能像画中一样一飞冲天，工作和生活都顺顺利利的。</p>
<p>这本书定价不高，28 块钱大概是一顿饭钱，或者是一杯奶茶，如果感兴趣你也可以买来看看，特别推荐第三部分哦~s</p>
