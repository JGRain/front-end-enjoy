<h3 id="_1-工作经历" tabindex="-1"><a class="header-anchor" href="#_1-工作经历" aria-hidden="true">#</a> 1. 工作经历</h3>
<p>这一年的工作经历，和以往有一个共同点：<strong>遇到过新的问题，然后有了新的体会和感受</strong>。</p>
<p>从 2014 年毕业，后裸辞工作之后自学前端，然后开始慢慢深入学习和开发，一直到如今 2021 年，我几乎每年都有工作变动，几乎都是自己主动发起的调整。</p>
<p>越来越发现，刚毕业的时候我们都充满热情，到最后大家却逐渐地对“妥协”二字妥协了。这几年互联网行业的确很卷，竞争力和压力也增加了不少，很多人都充满了迷茫和焦虑，而我也增加了不少的疑惑。</p>
<p>今年的主要思考是：</p>
<ul>
<li>作为一名前端开发/程序员，我想要走向哪里？</li>
<li>职业发展上常说的广度和深度，是不是伪命题？</li>
<li>团队管理中，一个开发能做的有多少？</li>
</ul>
<p>关于这些，每一个点讲起来都可以长篇大论了，我之前在博客有讲相关的内容，包括《关于一年一换的魔咒》、《技术开发的门槛高吗》、《关于技术开发的职业发展》、《技术深度是伪命题吗》，这些你可以在我的<a href="http://www.godbasin.com/front-end-work/front-end-days/about-front-end-11.html" target="_blank" rel="noopener noreferrer">“被删前端游乐场--前端这几年”<ExternalLinkIcon/></a>分享里找到。</p>
<h3 id="_2-项目经历" tabindex="-1"><a class="header-anchor" href="#_2-项目经历" aria-hidden="true">#</a> 2. 项目经历</h3>
<p>这一年的工作经历，和以往也有不同点：第一次接触大型前端项目的难题。</p>
<p>目前在文档团队，在线文档的编辑和协同对前端来说有不小的挑战。之前也有简单地整理了一篇了解在线文档的文章：<RouterLink to="/front-end-basic/deep-learning/why-spreadsheet-app-excited.html">《在线 Excel 项目到底有多刺激》</RouterLink>，简单来说会包括：</p>
<ul>
<li>协同过程中的冲突处理算法</li>
<li>多人协作时的版本管理和维护</li>
<li>大文档下的加载和渲染性能、卡顿问题</li>
<li>文档数据结构的设计和算法</li>
<li>Canvas 渲染和 DOM 渲染的一致性</li>
<li>排版引擎的设计和优化</li>
</ul>
<p>除了文档本身功能逻辑的难题之外，这样的项目还涉及到代码量过大（100W+）、开发团队人员过多、协作开发和管理等各种各样的难题，包括：</p>
<ul>
<li>如何对模块之间进行功能解耦</li>
<li>如何进行大项目的代码组织和架构设计</li>
<li>大型前端项目的代码加载流程如何优化</li>
<li>大团队里多人协作导致的问题和解决方案</li>
<li>如何保证大型项目的开发效率/可维护性/可读性</li>
</ul>
<p>团队里优秀的小伙伴很多，真就每天都能学到不少的知识。即使到今天，我已经来这个团队一年多了，依然对整个项目还有许多地方了解得比较浅。总的来说，<strong>非常有幸参与到这样的项目里，让我可以在前端领域工作的第 6 年里，依然有无数种让自己获得成长的方式</strong>。</p>
<p>以上便是是工作相关的，虽然今年也有介于团队调整空白期的懈怠，但在即将结束的 2021 年底前，顺利地将自己的状态调整过来，这是值得开心的事情。</p>
<h3 id="_3-工作外的技术成长" tabindex="-1"><a class="header-anchor" href="#_3-工作外的技术成长" aria-hidden="true">#</a> 3. 工作外的技术成长</h3>
