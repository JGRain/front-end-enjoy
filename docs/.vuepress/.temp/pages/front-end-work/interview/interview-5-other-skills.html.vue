<p>专业知识也好，项目经验也好，比较难体现日常工作中的一些能力。而通过一些其他的问题，面试官可以更加全面地掌握面试者的信息。</p>
<h2 id="逻辑思维" tabindex="-1"><a class="header-anchor" href="#逻辑思维" aria-hidden="true">#</a> 逻辑思维</h2>
<p>逻辑思维主要考察面试者思考的能力，是否结构化、是否有条理，表达描述是否清晰等。</p>
<h3 id="技术开放题" tabindex="-1"><a class="header-anchor" href="#技术开放题" aria-hidden="true">#</a> 技术开放题</h3>
<p>使用技术相关的开放题，既可以考察面试对象对技术的掌握和灵活使用的程度。可能会包括以下的问题（内容来自<a href="https://github.com/airuikun/Weekly-FE-Interview" target="_blank" rel="noopener noreferrer">Weekly-FE-Interview<ExternalLinkIcon/></a>）：</p>
<ul>
<li>一次性插入 1000 个 div，如何优化插入的性能</li>
<li>内存不足，一次只允许你装载和操作 1 亿条数据，如何对 100 亿条数据进行排序</li>
<li>在浏览器端，用 JS 存储 2 万个小球的信息，包含小球的大小，位置，颜色等，如何做到对这2万条小球信息进行最优检索和存储</li>
</ul>
<h3 id="设计方案" tabindex="-1"><a class="header-anchor" href="#设计方案" aria-hidden="true">#</a> 设计方案</h3>
<p>设计方案的方式，比较贴近工作过程，可以从多个角度考察面试者的知识面、问题瓶颈和边界情况的考虑。通常可能包括以下的内容：</p>
<ul>
<li>现在要做一个坦克对战的游戏，由你来设计，需要考虑哪些问题</li>
<li>如果让你做一个俄罗斯方块的游戏，讲讲具体的思路</li>
<li>做春节红包、秒杀活动中需要注意什么问题</li>
<li>现在需要通过配置的方式生成各种各样的 H5 活动页面，如何实现</li>
</ul>
<h3 id="逻辑思考" tabindex="-1"><a class="header-anchor" href="#逻辑思考" aria-hidden="true">#</a> 逻辑思考</h3>
<p>除了方案设计，一些逻辑推导的题目也可以作为考察的一部分。通常来说这类题目有时候让人觉得像脑筋急转弯，可能是这样的题目（有些从网上扒来的题目）：</p>
<ul>
<li>一个 5L、一个 6L 的瓶子，要得到 3L 的水，要怎么做</li>
<li>在一天之中，时针和分针共重叠多少次，时间分别是多少</li>
<li>1=5，2=15，3=215，4=2145，那么 5=?</li>
<li>现有 64 匹马，8 赛道，至少跑多少个回合可以得到前 4 名</li>
</ul>
<p>还有一些开放性的题目，包括：</p>
<ul>
<li>为什么下水道的盖子是圆的</li>
<li>一个是两种药片，每种有两个，一个人需要早上吃两种药片各一个，现在这四个药片混在一起了这个人什么方法吃</li>
<li>请把一盒蛋糕切成8份，分给8个人，但蛋糕盒里还必须留有一份</li>
</ul>
<h2 id="其他" tabindex="-1"><a class="header-anchor" href="#其他" aria-hidden="true">#</a> 其他</h2>
<p>除了项目内容，面试官有时候也会了解面试者的一些工作以外的情况。</p>
<h3 id="学习与技能" tabindex="-1"><a class="header-anchor" href="#学习与技能" aria-hidden="true">#</a> 学习与技能</h3>
<p>对于程序员来说，自学是很关键的一个能力，而面试过程可能会问到：</p>
<ul>
<li>平时业余时间有进行学习吗，学习方式是怎样的</li>
<li>会通过哪些途径来了解最新的前端技术</li>
<li>最近有看哪些书，描述一下相关内容</li>
<li>近一年有关注过什么新技术，简单介绍一下</li>
<li>这一年中做过最有挑战性的工作是什么，过程是怎样的</li>
<li>技术领域中最喜欢的东西是什么、为什么</li>
<li>日常工作有难度吗，难点在哪</li>
<li>你最喜欢的前端项目是什么、为什么</li>
<li>个人的职业规划是如何的</li>
</ul>
<h3 id="团队" tabindex="-1"><a class="header-anchor" href="#团队" aria-hidden="true">#</a> 团队</h3>
<ul>
<li>自己最擅长的是什么工作</li>
<li>你在这个项目中的角色是是什么</li>
<li>你觉得自己给团队带来了哪些价值、做了什么贡献</li>
<li>对新的团队有没有什么要求</li>
<li>团队合作中的代码可读性和维护性，你怎么看</li>
</ul>
